require 'spec_helper'

describe 'CuitEmpresarial' do
=begin
  it 'aplicar descuento en cuit empresarial descuenta correctamente' do
    porcentaje_descuento = 5
    importe_descontado = CuitParticular.new.aplicar_descuento(500)
    expect(importe_descontado).to eq (500 - 500%100*porcentaje_descuento)
  end
=end
end
